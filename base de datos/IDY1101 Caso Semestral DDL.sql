-- Tabla principal de datos de clientes
CREATE TABLE CLIENTES (
    IDCLIENTE       NUMBER PRIMARY KEY,
    RUT             VARCHAR2(15),
    NOMBRE          VARCHAR2(50) NOT NULL,
    APELLIDOPATERNO VARCHAR2(50),
    APELLIDOMATERNO VARCHAR2(50),
    TELEFONO        VARCHAR2(20),
    EMAIL           VARCHAR2(100),
    DIRECCION       VARCHAR2(200),
    CIUDAD          VARCHAR2(50),
    CODIGOPOSTAL    VARCHAR2(10),
    FECHAREGISTRO   VARCHAR2(50)
);

-- Tabla para transacciones financieras
CREATE TABLE TRANSACCIONES (
    TRANSACCIONID    NUMBER
        GENERATED BY DEFAULT ON NULL AS IDENTITY
    PRIMARY KEY,
    IDCLIENTE        NUMBER NOT NULL,
    MONTO            NUMBER(15) NOT NULL,
    FECHATRANSACCION VARCHAR2(50) NOT NULL,
    DETALLE          VARCHAR2(200),
    FOREIGN KEY ( IDCLIENTE )
        REFERENCES CLIENTES ( IDCLIENTE )
            ON DELETE CASCADE
);